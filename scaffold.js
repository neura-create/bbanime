// exactamente de la posicion de scroll del usuario (se congela si para de
// scrollear). Los 181 frames reales viven en assets/frames/ (extraidos del
// video del cliente con ffmpeg). Ejecutar con: node scaffold.js
'use strict';
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const FRAME_COUNT = 181;

function write(relPath, lines) {
  const full = path.join(ROOT, relPath);
  fs.mkdirSync(path.dirname(full), { recursive: true });
  fs.writeFileSync(full, lines.join('\n'), 'utf8');
  console.log('  creado:', relPath);
}

