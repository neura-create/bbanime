// Sparkles (estrellas) — set fijo de posiciones/colores
// ---------------------------------------------------------------------------
function buildSparkles() {
  var positions = [
    { x: '8%', y: '18%', c: 'pink', s: 22 },
    { x: '88%', y: '12%', c: 'cyan', s: 16 },
    { x: '15%', y: '72%', c: 'gold', s: 18 },
    { x: '92%', y: '68%', c: 'pink', s: 14 },
    { x: '48%', y: '8%', c: 'cyan', s: 12 },
    { x: '6%', y: '46%', c: 'gold', s: 12 },
    { x: '95%', y: '40%', c: 'pink', s: 18 },
    { x: '30%', y: '88%', c: 'cyan', s: 14 },
    { x: '70%', y: '90%', c: 'gold', s: 16 }
  ];
  var out = [];
  positions.forEach(function (p, i) {
    out.push(
      '    <svg class="spark spark-' + p.c + '" style="left:' + p.x + '; top:' + p.y +
      '; width:' + p.s + 'px; height:' + p.s + 'px; animation-delay:' + (i * 0.35) + 's;" ' +
      'viewBox="0 0 24 24" aria-hidden="true"><path d="M12 0 L14.5 9.5 L24 12 L14.5 14.5 L12 24 L9.5 14.5 L0 12 L9.5 9.5 Z"/></svg>'
    );
  });
  return out.join('\n');
}

